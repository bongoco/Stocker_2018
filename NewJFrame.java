    private void logOnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logOnActionPerformed
        // TODO add your handling code here:
        age = Integer.parseInt(jTextField3.getText());
        if (age<30)
        {
            userRisk +=2;
        }

        else if (age>30 && age<50)
        {
            userRisk +=0;
        }

        else if (age>50)
        {
            userRisk -=1;

        }

        else userRisk+=0;
        moneyInvested = Integer.parseInt(jTextField1.getText());
        amountLabel.setText( moneyInvested + "");
        ageLabel.setText(age + "");
        riskLabel.setText(userRisk + "");
    }//GEN-LAST:event_logOnActionPerformed

